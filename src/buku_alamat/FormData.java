    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        try{
            st = cn.createStatement();
            if(txtPemilik.getText().equals("") || 
                txtJalan.getText().equals("") ||
                txtNoRumah.getText().equals("") || txtNoHp.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Data tidak boleh kosong", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if (btnsimpan.getText() == "Simpan"){
            String  cek = "SELECT * FROM detail_alamat WHERE nama_pemilik = '" + txtPemilik.getText() + "'";
            rs = st.executeQuery(cek);
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "Ups... Nama Pemilik ini sudah ada");
            } else{
                String sql = "INSERT INTO detail_alamat VALUES ('" + txtPemilik.getText()+
                        "','" + txtJalan.getText() +
                        "','" + txtNoRumah.getText() +
                        "','" + txtNoHp.getText()+"')";
                
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            }
            TampilData();
            Bersih();
        }else{
            String update = "UPDATE detail_alamat SET nama_jalan = '" + txtJalan.getText() +
                        "', nomor_rumah = '" + txtNoRumah.getText() +
                        "', nomor_hp = '" + txtNoHp.getText()+
                        "' WHERE nama_pemilik = '" + txtPemilik.getText()+"'";
            
            st.executeUpdate(update);
            JOptionPane.showMessageDialog(null, "Data Berhasil Diupdate");
            Bersih();
            TampilData();
        }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
        
    }//GEN-LAST:event_btnsimpanActionPerformed

