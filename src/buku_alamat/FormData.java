    private void CariData(){
        try{
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM detail_alamat WHERE " 
                    + cmbCari.getSelectedItem().toString() 
                    + " LIKE '%" + txtCari.getText() + "%'");
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Pemilik");
            model.addColumn("Nama Jalan");
            model.addColumn("Nomor Rumah");
            model.addColumn("Nomor Hp");
            
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while (rs.next()){
                Object[] data = {
                    rs.getString("nama_pemilik"),
                    rs.getString("nama_jalan"),
                    rs.getString("nomor_rumah"),
                    rs.getString("nomor_hp")
                };
                model.addRow(data);
                tblData.setModel(model);
            }
        }catch(Exception e){
            
        }
    }
    
